#include <stdio.h>
/**
 * main - print lowercase alphabet
 * Description: use main function
 * this program prints lowercase alphabet
 * Return: 0
 */
int main(void)
{

	char ch;

	for (ch = 'a' ; ch <= 'z' ; ch++)

		putchar(ch);
	for (ch = 'A' ; ch <= '' ; ch++)
		putchar(ch); 
		putchar('\n');
	return (0);


}
